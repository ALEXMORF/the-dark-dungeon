int ranged_rand(int32 lo, int32 hi)
{
    int32 result = (quick_rand() % (hi - lo)) + lo;
    return result;
}

inline real32
real_quick_rand(real32 lo, real32 hi)
{
    real32 result;
    result = (real32)(quick_rand() % (int32)(hi - lo) + (int32)lo);
    return result;
}
